
public class Node {
	private int nodeNo;
	private int order;

	Node(int nodeNo,int order){
		this.nodeNo = nodeNo;
		this.order = order;
	}

	public int getNodeNo() {
		return nodeNo;
	}
	public void setNodeNo(int nodeNo) {
		this.nodeNo = nodeNo;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}



}
